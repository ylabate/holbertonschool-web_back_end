#!/usr/bin/env python3
"""Module for string concatenation with type annotations."""


def concat(str1: str, str2: str) -> str:
    """Concatenate two strings and return the result.

    This function takes two strings as arguments and returns their
    concatenation as a single string.

    Args:
        str1: The first string to concatenate.
        str2: The second string to concatenate.

    Returns:
        str: The concatenation of str1 and str2.
    """
    return str1 + str2
